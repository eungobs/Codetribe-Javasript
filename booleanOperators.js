// Boolean and Operators
let x = 8;
let y = 12;

console.log(x > y);
console.log(x <= y);
console.log(x === y);
console.log(x !== y);

let a = true;
let b = false;

console.log(a && b);
console.log(a || b);
console.log(!a);

let p = 10;
p += 5;
console.log(p);

p -= 3;
console.log(p);

p *= 2;
console.log(p);

p /= 4;
console.log(p);

p %= 3;
console.log(p);
