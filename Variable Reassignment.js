// Variable Reassignment
// Declaring a variable ‘favoriteColour’ and assign your favourite colour to it.
let favoriteColour = "blue";

// Print the value of favoriteColour.
console.log("Initial favorite colour:", favoriteColour);

// Reassign favouriteColour to a different colour.
favoriteColour = "green";

// Print the new value of favouriteColour.
console.log("Updated favorite colour:", favoriteColour);
